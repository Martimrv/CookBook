package model;

public class tag {
    private int id;
    private String name;

    public tag() {
    }

    // Constructor, getters, and setters

    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
